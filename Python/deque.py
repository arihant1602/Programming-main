a = deque("Hello")
print(a)
a.append("!")
a.appendleft("!!")
print(a)
a.pop()
a.popleft()
print(a)
a.extend('abc')
print(a)
a.extendleft('abc')
print(a)
a.pop()
a.pop()
a.pop()
a.extend('def')
print(a)
a.rotate(2)
print(a)
a.rotate(-4)
print(a)
